
def add(num1, num2):
    return float(float(num1) + float(num2))
def subtract(num1, num2):
    return float(float(num1) - float(num2))
def multiply(num1, num2):
    return float(float(num1) * float(num2))
def divide(num1, num2):
    if(num2 == 0):
        return None
    return float(float(num1) / float(num2))